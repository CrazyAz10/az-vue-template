/**
 * 替换多语言自定义内容
 * @param {String} text 多语言文案
 * @param {Object} keys 文案中需要替换的字段key和替换值 {}
 * @returns String
 * 例如：
 *  text: 您在第<%>acount</%>局中中奖
 *  keys: {acount: 8}
 *  return: 您在第8局中中奖
 */
export function rpt(text, keys) {
  console.log(text, keys)
  var t = text
  if (Object.keys(keys).length) {
    for (const i in keys) {
      var reg = new RegExp(`<%>${i}</%>`, 'mg')
      t = text.replace(reg, keys[i])
    }
  }
  return t
}

